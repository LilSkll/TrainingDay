/**
 * ============================================================================
 *  Абстракция LLM-провайдера.
 * ============================================================================
 *
 *  Каждый конкретный провайдер (Groq / OpenAI / Gemini / Mock) реализует
 *  один и тот же интерфейс `LLMProvider`. Благодаря этому оркестратор
 *  `ModelOrchestrator` может прозрачно переключать модели, а бизнес-логика
 *  приложения вообще не знает, кто именно генерирует текст.
 *
 *      ┌─────────────────────────┐
 *      │  Сервисы (program, ...) │  <- формируют prompt, парсят ответ
 *      └────────────┬────────────┘
 *                   │
 *      ┌────────────▼────────────┐
 *      │   ModelOrchestrator     │  <- выбирает провайдера, fallback'и
 *      └────────────┬────────────┘
 *                   │
 *      ┌────────────▼────────────┐
 *      │     LLMProvider         │  (интерфейс)
 *      └────────────┬────────────┘
 *                   │
 *   ┌───────┬───────┼───────┬────────┐
 * Groq  OpenAI  Gemini  Mock(offline)
 * ============================================================================
 */

export interface LLMMessage {
  role: 'system' | 'user' | 'assistant';
  content: string;
}

export interface LLMRequest {
  messages: LLMMessage[];
  /** Целевая температура (0 = максимально детерминированно). */
  temperature?: number;
  /** Жёсткий лимит токенов в ответе. */
  maxTokens?: number;
}

export interface LLMResponse {
  text: string;
  /** Имя провайдера, фактически ответившего (для прозрачности в UI). */
  provider: string;
  /** Идентификатор модели. */
  model: string;
}

/** Контракт любого LLM-провайдера в системе. */
export interface LLMProvider {
  /** Уникальное имя, используется в логах и UI. */
  readonly name: string;

  /** Готов ли провайдер обслуживать запросы (есть ли ключ и т.п.). */
  isAvailable(): boolean;

  /** Синхронный текстовый completion. */
  complete(req: LLMRequest): Promise<LLMResponse>;
}
