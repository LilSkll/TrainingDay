/**
 * ============================================================================
 *  Off-topic guard — серверная защита чата от нерелевантных запросов.
 * ============================================================================
 *
 *  Зачем: даже с усиленным системным промптом модель иногда «поддаётся» и
 *  отвечает на посторонние темы (рецепты, политика, программирование).
 *  Этот модуль перехватывает явно нерелевантные сообщения ЕЩЁ ДО обращения
 *  к LLM и возвращает заготовленный вежливый отказ. Это:
 *    1. гарантирует отказ (двойная защита вместе с промптом);
 *    2. экономит токены и время ответа;
 *    3. даёт единый тон отказа независимо от настроения модели.
 *
 *  Логика намеренно консервативна: блокируем ТОЛЬКО явно off-topic.
 *  Спорные запросы (где может быть фитнес-контекст) пропускаем к модели —
 *  пусть промпт разбирает.
 * ============================================================================
 */

/** Стандартный вежливый отказ. */
export const OFFTOPIC_REPLY =
  'Я — AI-тренер и отвечаю только про тренировки, восстановление и спортивное питание. ' +
  'Задайте вопрос по теме — например, как адаптировать программу, заменить упражнение или что делать при боли.';

export interface GuardResult {
  /** true — запрос признан off-topic, отдать OFFTOPIC_REPLY без вызова LLM. */
  blocked: boolean;
  /** Понятная причина (для логов; пользователю не показываем). */
  reason?: string;
}

/**
 * Ключевые слова и фразы, однозначно указывающие на нерелевантную тему.
 *  Если встречаются в сообщении пользователя — блокируем.
 *
 *  Группы: кулинария, политика/религия, программирование/технологии,
 *  наука не о спорте, медицина/диагностика, финансы, общие знания.
 */
const OFFTOPIC_PATTERNS: string[] = [
  // Кулинария / еда не как спорт-питание
  'как приготовить', 'рецепт', 'плов', 'борщ', 'пирог', 'суп ', 'котлет',
  'блины', 'салат', 'соус', 'теста', 'выпечк', 'десерт', 'торт',
  // Политика / религия / история
  'путин', 'байден', 'трамп', 'зеленск', 'выборы', 'парламент', 'вопрос ',
  'религи', 'бог ', 'церковь', 'война', 'политик',
  // Программирование / технологии / код
  'код', 'программирование', 'напиши код', 'язык программирования',
  'javascript', 'python', 'react', 'html', 'css',
  'алгоритм', 'баг', 'функци', 'компилятор', 'сайт сделать', 'приложение сделать',
  // Наука / общие знания
  'ядерн', 'реактор', 'квант', 'теорем', 'уравнени', 'днк', 'клетк',
  'физика', 'химия', 'астроном', 'планет', 'вселенн', 'чёрная дыра',
  'как устроен', 'принцип работы', 'откуда взял',
  // Медицина / диагнозы
  'диагноз', 'болезнь', 'симптом', 'таблетк', 'лекарств', 'анализ сдал',
  'давление ', 'что мне лечить', 'как вылечить',
  // Финансы / крипта
  'крипт', 'биткоин', 'акци', 'вклад', 'кредит', 'налог', 'зарплат',
  'сколько стоит', 'заработа', 'инвестиц',
  // Общая болтовня
  'расскажи сказк', 'напиши стих', 'напиши песн', 'анекдот', 'шутк',
  'поздравь', 'придумай истор',
];

/** Короткие слова-маркеры, требующие проверки контекста. */
const STRONG_OFFTOPIC_WORDS = new Set([
  'плов', 'борщ', 'котлеты', 'блины', 'оладьи', 'вареники',
  'биткоин', 'эфириум',
  'анекдот',
]);

/**
 * Оценивает сообщение пользователя на off-topic.
 *  Возвращает { blocked: true } только при УВЕРЕННОМ определении.
 */
export function checkOffTopic(message: string): GuardResult {
  const text = ' ' + message.toLowerCase().trim() + ' ';

  // 1. Точное вхождение ключевых фраз.
  for (const pattern of OFFTOPIC_PATTERNS) {
    if (text.includes(pattern)) {
      return { blocked: true, reason: `matched pattern "${pattern}"` };
    }
  }

  // 2. Отдельные сильные слова (как самостоятельные токены).
  const words = text.split(/\s+/);
  for (const w of words) {
    if (STRONG_OFFTOPIC_WORDS.has(w)) {
      return { blocked: true, reason: `strong offtopic word "${w}"` };
    }
  }

  // 3. Всё остальное пропускаем — пусть промпт разбирает спорные случаи.
  return { blocked: false };
}
